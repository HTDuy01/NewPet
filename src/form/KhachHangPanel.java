    private void CustomerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CustomerTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) CustomerTable.getModel();

        String id = tblModel.getValueAt(CustomerTable.getSelectedRow(), 0).toString();
        String name = tblModel.getValueAt(CustomerTable.getSelectedRow(), 1).toString();
        String sex = tblModel.getValueAt(CustomerTable.getSelectedRow(), 2).toString();
        String address = tblModel.getValueAt(CustomerTable.getSelectedRow(), 3).toString();
        String phone = tblModel.getValueAt(CustomerTable.getSelectedRow(), 4).toString();

        txtID.setText(id);
        txtName.setText(name);
        cbSex.setSelectedItem(sex);
        txtAddress.setText(address);
        txtPhone.setText(phone);
    }//GEN-LAST:event_CustomerTableMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            customerList = CustomerDAO.searchCustomer(txtSearch.getText());
            tableModel.setRowCount(0);

            customerList.forEach(customer -> {
                tableModel.addRow(new Object[]{customer.getId(), customer.getFullName(), customer.getSex(), customer.getAddress(), customer.getPhone()});
            });
            resetTextField();
        } catch (SQLException ex) {
            Logger.getLogger(KhachHangPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String id = txtID.getText();
        String name = txtName.getText();
        String sex = (String) cbSex.getSelectedItem();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();
        Customer cus = new Customer(id, name, sex, address, phone);
        tableModel.addRow(new String[]{id, name, sex, address, phone});
        CustomerDAO.insertCustomer(cus);
        try {
            showCustomer();
            resetTextField();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (CustomerTable.getSelectedRowCount() == 1) {
            String id = txtID.getText();
            String name = txtName.getText();
            String sex = (String) cbSex.getSelectedItem();
            String address = txtAddress.getText();
            String phone = txtPhone.getText();
            Customer cus = new Customer(id, name, sex, address, phone);
            CustomerDAO.updateCustomer(cus);

            JOptionPane.showMessageDialog(this, "Update Successfully");
            try {
                showCustomer();
                resetTextField();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = CustomerTable.getSelectedRow();
        if (selectedIndex >= 0) {
            Customer cus = customerList.get(selectedIndex);
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this Customer Data?");
            System.out.println("Option: " + option);
            if (option == 0) {
                CustomerDAO.deleteCustomer(cus.getId());
                try {
                    showCustomer();
                    resetTextField();
                } catch (SQLException e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


