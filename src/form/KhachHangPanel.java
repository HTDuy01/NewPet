    public void showCustomer() throws SQLException {
        customerList = CustomerDAO.finALL();

        tableModel.setRowCount(0);

        customerList.forEach(customer -> {
            tableModel.addRow(new Object[]{customer.getId(), customer.getFullName(), customer.getSex(), customer.getAddress(), customer.getPhone()});
        });
    }
